package br.com.curso_programacao.aplicacao;

import java.util.Locale;
import java.util.Scanner;

public class Mat_Exercicio5 {

	public static void main(String[] args) {
		
		Scanner input = new Scanner(System.in);
		
		Locale.setDefault(Locale.US);
		
		int cod_peca1, cod_peca2, quant_peca1, quant_peca2;
		double preco_peca1, preco_peca2, preco_total;
		
		cod_peca1 = input.nextInt();
		cod_peca2 = input.nextInt();
		quant_peca1 = input.nextInt();
		quant_peca2 = input.nextInt();
		preco_peca1 = input.nextDouble();
		preco_peca2 = input.nextDouble();
		
		preco_total = quant_peca1*preco_peca1 + quant_peca2*preco_peca2;
		
		System.out.printf("VALOR A PAGAR: R$ %.2f", preco_total);
		
		input.close();

	}

}
